public enum PartnerType {

	AGENT(0, "代理商"), ADVERTISER(1, "直客"), OEM(2, "OEM");

	private PartnerType(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	private final int code;
	private final String desc;

	public int getCode() {
		return code;
	}

	public String getDesc() {
		return desc;
	}
}
