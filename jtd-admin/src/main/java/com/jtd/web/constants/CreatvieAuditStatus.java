public enum CreatvieAuditStatus {
	PASS(0,"通过"),SOMEPASS(1,"部分通过");
	private CreatvieAuditStatus(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	private final int code;
	private final String desc;

	public int getCode() {
		return code;
	}

	public String getDesc() {
		return desc;
	}
}
