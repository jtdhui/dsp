public enum AuditType {

	NOADUIT(0, "创意不需要审核"),ADUIT(1,"创意需要审核");
	
	private AuditType(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	private final int code;
	private final String desc;

	public int getCode() {
		return code;
	}

	public String getDesc() {
		return desc;
	}
}
